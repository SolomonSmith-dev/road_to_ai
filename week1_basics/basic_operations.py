# Arithmetic operations
x = 10
y = 3

add = x + y           # Addition
subtract = x - y      # Subtraction
multiply = x * y      # Multiplication
divide = x / y        # Division (returns float)
power = x ** y        # Exponentiation
floor_divide = x // y # Floor division
remainder = x % y     # Modulus

print("Addition:", add)
print("Subtraction:", subtract)
print("Multiplication:", multiply)
print("Division:", divide)
print("Exponentiation:", power)
print("Floor Division:", floor_divide)
print("Modulus:", remainder)
